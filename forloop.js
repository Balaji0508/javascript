//for loop
let arr=[10,20,30,40,50,60,70];
console.log(arr);
for(i=1;i<arr.length;i++){
    console.log(i);
}

//for each
let arr=[10,20,30,40,50,60,70];
arr.forEach(function(element){
    console.log(element);
})

//while loop
let arr=[1,2,3,4,5,6,]
let i=0;
while(i<arr.length){
    console.log(arr[i]);
    i++
}

//do while loop
let arr=[1,2,3,4,5,6,]
let i=0;
do{
    console.log(arr[i]);
    i++
}while(i<arr.length);


